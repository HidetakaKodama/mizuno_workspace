
public class Ex1_A2 {
	public static void main(String[] args) {
		String st1 = "9";
		String st2 = "5";

		int i1 = Integer.parseInt(st1);
		int i2 = Integer.parseInt(st2);

		System.out.println(i1 + i2);
		System.out.println(i1 - i2);
		System.out.println(i1 * i2);
		System.out.println(i1 / i2);
		System.out.println(i1 % i2);
	}
}
